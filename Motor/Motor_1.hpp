// =========================================================
// 一、LEDC（ESP32 硬件 PWM）参数配置
// =========================================================
//
// LEDC_FREQ：PWM 频率
//   - 设置为 20kHz，可避开人耳可听频率
//   - 电机运行更安静
//
// LEDC_BIT：PWM 分辨率
//   - 8bit 表示 PWM 范围为 0 ~ 255
// =========================================================
#define LEDC_FREQ 20000
#define LEDC_BIT 8

// =========================================================
// 二、Motor 类定义
// =========================================================
class Motor
{
private:
    // -----------------------------------------------------
    // 电机控制引脚
    // motorPIN1 / motorPIN2 通常接 H 桥驱动的 IN1 / IN2
    // -----------------------------------------------------
    int motorPIN1, motorPIN2;

    // -----------------------------------------------------
    // ESP32 的 LEDC 通道号
    // 每个通道对应一路硬件 PWM
    // -----------------------------------------------------
    int channel1, channel2;

public:
    // -----------------------------------------------------
    // 构造函数
    // 参数说明：
    //   mpin1, mpin2 : 电机控制引脚
    //   ch1, ch2     : LEDC 通道号（一般 0、1 即可）
    // -----------------------------------------------------
    Motor(int, int, int, int);

    // -----------------------------------------------------
    // 电机运行函数
    // 参数 pwm：
    //   > 0 ：正转
    //   < 0 ：反转
    // -----------------------------------------------------
    void run(int);

    // 马达刹车（高阻，快速停）
    void brake();

    // 马达熄火（无阻力，自由滑行）
    void flameout();
};

// =========================================================
// 三、构造函数实现
// =========================================================
//
// 在创建 Motor 对象时自动执行
// 用于完成：
//   1. LEDC 通道配置
//   2. PWM 引脚绑定
// =========================================================
Motor::Motor(int mpin1, int mpin2, int ch1 = 0, int ch2 = 1)
{
    // 保存引脚和通道号
    motorPIN1 = mpin1;
    motorPIN2 = mpin2;
    channel1 = ch1;
    channel2 = ch2;

    // -----------------------------------------------------
    // 配置 LEDC 通道
    // ledcSetup(通道号, 频率, 分辨率)
    // -----------------------------------------------------
    ledcSetup(channel1, LEDC_FREQ, LEDC_BIT);
    ledcSetup(channel2, LEDC_FREQ, LEDC_BIT);

    // -----------------------------------------------------
    // 将 LEDC 通道绑定到具体 IO 引脚
    // -----------------------------------------------------
    ledcAttachPin(motorPIN1, channel1);
    ledcAttachPin(motorPIN2, channel2);
}

// =========================================================
// 四、电机运行控制（核心函数）
// =========================================================
//
// 控制思想（非常重要，教学重点）：
//   电机控制 = 方向 + 速度
//
//   方向：由正负号决定
//   速度：由 PWM 大小决定
// =========================================================
void Motor::run(int pwm)
{
    // 限制 PWM 范围，防止非法输入
    pwm = constrain(pwm, -255, 255);

    if (pwm >= 0)
    {
        // -------------------------------
        // 正转：通道 1 输出 PWM
        //       通道 2 输出 0
        // -------------------------------
        ledcWrite(channel1, pwm);
        ledcWrite(channel2, 0);
    }
    else
    {
        // -------------------------------
        // 反转：通道 2 输出 PWM
        //       通道 1 输出 0
        // -------------------------------
        ledcWrite(channel1, 0);
        ledcWrite(channel2, -pwm);
    }
}

// =========================================================
// 五、电机刹车（高阻）
// =========================================================
//
// 两个通道同时拉高
// 电机会被“硬性刹住”，停止很快
// =========================================================
void Motor::brake()
{
    ledcWrite(channel1, 255);
    ledcWrite(channel2, 255);
}

// =========================================================
// 六、电机熄火（无阻力）
// =========================================================
//
// 两个通道同时关闭
// 电机可以自由滑行
// =========================================================
void Motor::flameout()
{
    ledcWrite(channel1, 0);
    ledcWrite(channel2, 0);
}
